#include "xc.h"

void SetupPins(void)
{
    /****************************************************************************
     * Setting the Output Latch SFR(s) *Writes to pins
     ***************************************************************************/
    LATA = 0x0000;
    LATB = 0x0000;

    /****************************************************************************
     * Setting the GPIO Direction SFR(s) *Sets pins as input (1) or output (0)
     ***************************************************************************/
    TRISA = 0x0003;
    TRISB = 0x00EC; //FFFF

    /****************************************************************************
     * Setting the Weak Pull Up and Weak Pull Down SFR(s) 
     ***************************************************************************/
    CNPDA = 0x0000;
    CNPDB = 0x0000;
    CNPUA = 0x0000;
    CNPUB = 0x0000;

    /****************************************************************************
     * Setting the Open Drain SFR(s) *Allows for outputs other than Vdd
     ***************************************************************************/
    ODCA = 0x0000;
    ODCB = 0x0000;

    /****************************************************************************
     * Setting the Analog/Digital Configuration SFR(s) *Sets pins as analog input (1) or digital (0)
     ***************************************************************************/
    ANSELA = 0x0003; //0017
    ANSELB = 0x000C; //038C
}